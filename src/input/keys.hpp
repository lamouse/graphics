// 辅助函数：异或两个 enum class 值
constexpr auto operator|(Modifiers lhs, Modifiers rhs) -> Modifiers {
    using T = std::underlying_type_t<Modifiers>;
    return static_cast<Modifiers>(static_cast<T>(lhs) | static_cast<T>(rhs));
}

// 也可以定义 ^= 运算符
inline auto operator|=(Modifiers& lhs, Modifiers rhs) -> Modifiers& {
    lhs = lhs | rhs;
    return lhs;
}

inline auto hasFlag(Modifiers value, Modifiers flag) -> bool {
    using T = std::underlying_type_t<Modifiers>;
    return (static_cast<T>(value) & static_cast<T>(flag)) != 0;
}

